// Number constants

console.log(Math.PI);  // 3.14159
console.log(Math.E);  //  2.718
console.log(Math.LN2);  // 0.693


// Rounding methods
console.log(Math.ceil(89.765))  // rounds up to the closest integer -> 90
console.log(Math.floor(89.765))  // rounds down to the closest integer -> 88
console.log(Math.round(89.765))  // rounds up to the closest integer if the decimal is .5 or above; otherwise, rounds down to the closest integer 
console.log(Math.trunc(89.765))  // trims the decimal, leaving only the integer -> 89


// Arithmetic and calculus methods
console.log(Math.pow(2, 3))  // calculates the number 2 to the power of 3, the result is 8 
console.log(Math.sqrt(16))  // calculates the square root of 16, the result is 4 
console.log(Math.cbrt(8))  // finds the cube root of 8, the result is 2  
console.log(Math.abs(-10))  // returns the absolute value, the result is 10 

